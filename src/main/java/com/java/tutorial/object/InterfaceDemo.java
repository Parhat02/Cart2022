package com.java.tutorial.object;

public interface InterfaceDemo {
    //objects define their interaction with the outside world through the method that they expose.
    //Methods from the object's interface with the outside world.
   //A java interface is a bit like a class, except a java interface can only contain method signature and fields.
 //A Java interface cannot contain an implementation of the methods.
 //only the signature (name, parameters, and exceptions) of the method.

   double addTowNumbers(double a, double b);
   double multiplyTowNumbers(double a, double b);
   float divideTwoNumbers(double a, double b);
   double subtractTowNumbers(double a, double b);
 }
